
public class KNpackages {
	private int weight;
	private int value;  
	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public KNpackages(int weight, int value) {
		super();
		this.weight = weight;
		this.value = value;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
